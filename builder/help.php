<div class="restore-session">
	<h2>Restore Session</h2>
	<p class="desc">Attempts to restore your previous browser session of Celestia Content Builder.</p>
	<cite>notes</cite>
	<ul class="notes">
		<li>If the "Restore Session" is clicked, Celestia will attempt to restore your previous session.</li>
		<li>When Celestia Content Builder is loaded up, a session is created. After you choose a site to work on and either create or load a package your session is saved.</li>
		<li>If your browsers crashes or you accidentally close your browser and a session was created, the next time you open your browser Celestia will try to find a valid session. If a valid session is found the "Restore Session" button will appear on the Create / Open dialog.</li>
	</ul>
</div>
